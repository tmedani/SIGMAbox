function s = Num2on_off(i)
%%%------------------------------------------------------------------------
%   s = Num2on_off(i)
%
%   Function task:
%   Convert a boolean 0 or 1 into a 'on' and 'off' string
%   
%   Inputs :
%   i : boolean 0 or 1
%
%   Outputs : 
%   s : a string == 'on' or 'off'
%   handles : structure containing GUI informations
%
%--------------------------------------------------------------------------
%
%
%   Main Variables
%       
%
%   Dependencies
%       
%
%   NB: this code is copyrighted. 
%   Please refer to copyright info in the file footer.
%%%------------------------------------------------------------------------
if(i == 0)
    s = 'off';
elseif(i == 1)
    s = 'on';
end

end

